# Las anémonas pertenecen al Phylum Cnidaria, en el cual también se encuentran las medusas, los corales y las hidras, este grupo se caracteriza por tener células urticantes llamadas cnidocitos, los cuales segregan toxinas, como las actinoporinas que actúan haciendo poros en membrana plasmática, citolisinas que actúan en la célula causando lisis (ruptura de membrana), o destrucción de eritrocitos hemolisinas. Estos organismos representan una alternativa interesante de estudio, son una fuente de principios bioactivos y pueden desarrollar un papel importante en el campo de la química, bioquímica, farmacología y biotecnología, también nos pueden proporcionar información sobre procesos de regeneración celular. Las clasificaciones basadas con caracteres morfológicos no siempre coinciden con las basadas con secuencias nucleotídicas (Morandini 2012). Para la identificación de especies de anémonas no basta con la clasificación morfológica, ya que la identificación de la mayoría de los hexacorales de cuerpo blando requiere exámenes morfológicos e histológicos de especímenes preservados y experiencia para la correcta interpretación de las características observadas (Haussermann 2004).
